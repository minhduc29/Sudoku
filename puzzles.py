def is_valid_sudoku(board):
    """Check for validation of sudoku board"""
    rows = [{} for i in range(9)]
    columns = [{} for i in range(9)]
    boxes = [{} for i in range(9)]

    for i in range(9):
        for j in range(9):
            if board[i][j] != 0:
                box_index = (i // 3) * 3 + j // 3

                rows[i][board[i][j]] = rows[i].get(board[i][j], 0) + 1
                columns[j][board[i][j]] = columns[j].get(board[i][j], 0) + 1
                boxes[box_index][board[i][j]] = boxes[box_index].get(board[i][j], 0) + 1

                if rows[i][board[i][j]] > 1 or columns[j][board[i][j]] > 1 or boxes[box_index][board[i][j]] > 1:
                    return False

    return True